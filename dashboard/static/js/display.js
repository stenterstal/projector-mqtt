function clearNotifications(){
    // Clear all previous disruptions
    $('#notifications').empty()
}

function addNotification(title, subtitle, icontype=null){
    // Construct element
    let containerElement = $('<div class="notification">')
    // Text
    let textElement = $('<div class="text">')
    textElement.append($('<p>').text(title))
    textElement.append($('<span>').text(subtitle))
    // Icon
    let iconElement = getIcon(icontype)

    // Append to container
    containerElement.append(iconElement)
    containerElement.append(textElement)

    // Append to container
    $('#notifications').append(containerElement)
}

